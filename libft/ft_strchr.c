#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	j;

	i = ft_strlen(s);
	j = 0;
	while (j <= i)
	{
		if (s[j] == (char)c)
			return (&((char *)s)[j]);
		j++;
	}
	return (0);
}
